#include <stdio.h>

/**
* Add - add two integers
* @a: first int
* @b: second int
* Return: sum
*/

int add(int a, int b)
{
        return (a + b);
}
/**
* Sub - subtract two integers
* @a: first int
* @b: second int
* Return: sub
*/

int sub(int a, int b)
{
        return (a - b);
}
/**
* Mul - multiply two integers
* @a: first int
* @b: second int
* Return: mul
*/

int mul(int a, int b)
{
        return (a * b);
}
/**
* Mod - Modulus of two integers
* @a: first int
* @b: second int
* Return: mod
*/

int mod(int a, int b)
{
        return (a % b);
}
/**
* Div - add two integers
* @a: first int
* @b: second int
* Return: div
*/

int div(int a, int b)
{
        return (a / b);
}
